from __future__ import annotations

import numpy as np
import pandas as pd

from .model import PCSSOR, PCSSORParams


def calibrate_residual_quantile(
    train_df: pd.DataFrame,
    valid_df: pd.DataFrame,
    params: PCSSORParams,
    quantile: float = 0.90,
) -> float:
    """Validation-calibrated residual quantile half-width."""
    cal_model = PCSSOR(params).fit(train_df)
    pred = cal_model.predict(valid_df)
    residuals = np.abs(valid_df["Cp"].values - pred)
    return float(np.quantile(residuals, quantile))


def add_symmetric_interval(df: pd.DataFrame, pred_col: str, half_width: float, level_name: str = "90") -> pd.DataFrame:
    out = df.copy()
    out[f"Cp_lo_{level_name}"] = out[pred_col] - half_width
    out[f"Cp_hi_{level_name}"] = out[pred_col] + half_width
    return out