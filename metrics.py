from __future__ import annotations

import numpy as np
from sklearn.metrics import mean_absolute_error, mean_squared_error, r2_score


def compute_metrics(y_true, y_pred) -> dict[str, float]:
    y_true = np.asarray(y_true, dtype=float)
    y_pred = np.asarray(y_pred, dtype=float)
    mse = mean_squared_error(y_true, y_pred)
    return {
        "MSE": float(mse),
        "RMSE": float(np.sqrt(mse)),
        "MAE": float(mean_absolute_error(y_true, y_pred)),
        "R2": float(r2_score(y_true, y_pred)),
        "RelL2": float(np.linalg.norm(y_true - y_pred) / (np.linalg.norm(y_true) + 1e-12)),
    }


def per_alpha_metrics(df, pred_col: str = "Cp_pred", true_col: str = "Cp"):
    rows = []
    for alpha in sorted(df["Alpha"].unique()):
        sub = df[df["Alpha"] == alpha]
        m = compute_metrics(sub[true_col].values, sub[pred_col].values)
        rows.append({"Alpha": float(alpha), **m})
    return rows


def empirical_interval_coverage(y_true, lower, upper) -> float:
    y_true = np.asarray(y_true, dtype=float)
    lower = np.asarray(lower, dtype=float)
    upper = np.asarray(upper, dtype=float)
    return float(np.mean((lower <= y_true) & (y_true <= upper)))


def mean_interval_width(lower, upper) -> float:
    return float(np.mean(np.asarray(upper, dtype=float) - np.asarray(lower, dtype=float)))